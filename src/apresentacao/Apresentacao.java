    private void askIconSize() {
        JPanel panel = new JPanel();
        JTextField txt = new JTextField(10);
        JLabel lbl = new JLabel("Qual o tamanho dos ícones?");
        panel.add(lbl);
        panel.add(txt);
        String[] options = {"OK"};
        int ok = JOptionPane.showOptionDialog(null, panel, "", JOptionPane.NO_OPTION, 
                JOptionPane.QUESTION_MESSAGE, null, options, options[0]);
        if (ok == JOptionPane.CLOSED_OPTION) System.exit(0);
        else {
            try {
                double size = Double.parseDouble(txt.getText());
                window.setIconSize(size);
            } catch (NumberFormatException nfe){
                JOptionPane.showMessageDialog(this,"Por favor, digite um valor válido.");
                askIconSize();
            } catch (IllegalArgumentException iae){
                JOptionPane.showMessageDialog(this, iae.getMessage());
                askIconSize();
            }
        }
    }
    
    private void updateAll() {
        updateWindow();
        updateRegions();
        updateIcons();
        updateClicks();
    }
    
    private void updateWindow() {
        String str = window.printRegions() + window.printIcons();
        jTextArea1.setText("Tamanho dos ícones: " + window.getIconSize() + "\n\n\n" + str);
    }
    
    private void updateRegions() {
        jComboBox1.setModel(new DefaultComboBoxModel(window.getRegions().toArray()));
    }
    
    private void updateIcons() {
        jComboBox2.setModel(new DefaultComboBoxModel(window.getIcons().toArray()));
    }
    
    private void updateClicks() {
        String str = window.toString() + "\nTotal de cliques: " + window.getClicks().size();
        jTextArea2.setText(str);
    }
    
