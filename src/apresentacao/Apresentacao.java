    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        Icon icon = (Icon) jComboBox2.getSelectedItem();
        if (icon == null) {
            JOptionPane.showMessageDialog(this,"Por favor, selecione um ícone.");
        } else {
            window.removeIcon(icon);
            updateWindow();
            updateIcons();
            JOptionPane.showMessageDialog(this, "Ícone removido com sucesso!");
        }
    }//GEN-LAST:event_jButton4ActionPerformed

    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        try {
            MouseClick click = window.addClick(Integer.parseInt(jTextField7.getText()), 
                    Integer.parseInt(jTextField8.getText()));
            updateClicks();
            JOptionPane.showMessageDialog(this, "Clique em: " + click.printSelectedItem() 
                                            + " adicionado com sucesso!");
        } catch (NumberFormatException nfe){
            JOptionPane.showMessageDialog(this, "Por favor, digite os valores que deseja adicionar.");
        } catch (IllegalArgumentException iae){
            JOptionPane.showMessageDialog(this, iae.getMessage());
        }
    }//GEN-LAST:event_jButton5ActionPerformed

